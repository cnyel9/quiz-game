// Fungsi untuk mengambil pertanyaan dari kategori tertentu
function getQuestionsByCategory(category) {
  const selectedCategory = quizData.find((cat) => cat.category === category);
  return selectedCategory ? selectedCategory.questions : [];
}

// Fungsi untuk mendapatkan daftar kategori
function getCategories() {
  return quizData.map((cat) => cat.category);
}

// Fungsi untuk mengacak pertanyaan
function shuffleQuestions(questions) {
  return questions.sort(() => Math.random() - 0.5);
}

// Fungsi untuk mengambil sejumlah pertanyaan acak
function getRandomQuestions(count = 5) {
  const allQuestions = quizData.flatMap((category) => category.questions);
  return shuffleQuestions(allQuestions).slice(0, count);
}

// Tambahkan kategori baru dengan mudah
function addCategory(category, questions) {
  quizData.push({
    category: category,
    questions: questions,
  });
}

